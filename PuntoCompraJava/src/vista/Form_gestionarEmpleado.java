    private void jbtn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_guardarActionPerformed
        RegistroEmpleado regEmp = new RegistroEmpleado();
        Empleado empleado = new Empleado();
        int comuna = 0;
        int provincia = 0;
        int region = 0;

        empleado.setNumRutEmpleado((Integer.valueOf(jtxt_numRut.getText())));
        empleado.setDvRutEmpleado(jtxt_dvRut.getText());
        empleado.setpNombreEmpleado(jtxt_nombre.getText());
        empleado.setpApellidoEmpleado(jtxt_aPaterno.getText());
        empleado.setsApellidoEmpleado(jtxt_aMaterno.getText());
        empleado.setEmailEmpleado(jtxt_mail.getText());
        empleado.setCelularEmpleado(Integer.valueOf(jtxt_celular.getText()));
        empleado.setFechaNacEmpleado(jdate_fecNac.getDate());
        empleado.setDireccionEmpleado(jtxt_direccion.getText());
        //Setea comuna segun código en la DB
        switch (String.valueOf(jcmbo_comuna.getSelectedItem())) {
            case "Viña del Mar":
                comuna = 100;
                break;
            case "Quilpué":
                comuna = 101;
                break;
            case "Santiago":
                comuna = 102;
                break;
            default:
                throw new AssertionError();
        }
        empleado.setCodComuna(comuna);
        //Setea provincia segun código en la DB
        switch (String.valueOf(jcmbo_provincia.getSelectedItem())) {
            case "Valparaíso":
                provincia = 100;
                break;
            case "Marga-marga":
                provincia = 101;
                break;
            case "Talca":
                provincia = 103;
                break;
            case "Putre":
                provincia = 104;
                break;
            case "Limache":
                provincia = 106;
                break;
            case "Santiago":
                provincia = 107;
                break;
            default:
                throw new AssertionError();
        }
        empleado.setCodProvincia(provincia);
        //Setea region segun código en la DB
        switch (String.valueOf(jcmbo_region.getSelectedItem())) {
            case "Valparaíso":
                region = 10;
                break;
            case "RM":
                region = 11;
                break;
            case "Arica":
                region = 12;
                break;
            case "Maule":
                region = 13;
                break;
            default:
                throw new AssertionError();
        }
        empleado.setCodRegion(region);
        empleado.setDireccionEmpleado(jtxt_direccion.getText());
        empleado.setUsuarioEmpleado(jtxt_usuario.getText());
        empleado.setContrasenaEmpleado(jtxt_contraseña.getText());
        
        regEmp.agregarEmpleado(empleado);
    }//GEN-LAST:event_jbtn_guardarActionPerformed

