    private void jbtn_guardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_guardarCambiosActionPerformed
        RegistroProducto rp = new RegistroProducto();
        Producto pr = new Producto();       
        pr.setCodProducto((this.jtxt_codigo1.getText()));
        pr.setDescripcionProducto(this.jtxt_descripcion1.getText());
        pr.setPrecioUnitario((Integer.valueOf(this.jtxt_precio1.getText())));
        pr.setStock((Integer.valueOf(this.jtxt_stock1.getText())));
        pr.setCodigoCategoria((Integer.valueOf(this.jtxt_codCategoria1.getText())));
        pr.setNumRazonSocialProv((Integer.valueOf(this.jtxt_codProveedor1.getText())));
        pr.setPorcentajeDescuento((Double.valueOf(this.jtxt_porcentajeDescuento1.getText())));
        if (rp.actualizarProducto(pr,jtxt_nombreBuscar.getText())) {
            JOptionPane.showMessageDialog(null,"Producto Actualizado","Actualizar producto",1);
            System.out.println(pr.toString());
        }
        else{
            JOptionPane.showMessageDialog(null,"Producto No Actualizado","Actualizar producto",0);
        }
        
    }//GEN-LAST:event_jbtn_guardarCambiosActionPerformed

