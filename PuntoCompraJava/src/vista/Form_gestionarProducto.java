    private void jbtn_guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_guardar1ActionPerformed
        RegistroProducto regProd = new RegistroProducto();
        String codProducto = ((jtxt_codigo.getText()));
        String descripcionProducto = (jtxt_descripcion.getText());
        int precioUnitario = ((Integer.valueOf(jtxt_precio.getText())));
        int stock = ((Integer.valueOf(jtxt_stock.getText())));
        int codCategoria = ((Integer.valueOf(jtxt_codCategoria.getText())));
        int numRazonSocialProv = ((Integer.valueOf(jtxt_codProveedor.getText())));
        double porcentajeDescuento = ((Double.valueOf(jtxt_porcentajeDescuento.getText())));
        Producto prod = new Producto(codProducto, precioUnitario, stock, codCategoria, numRazonSocialProv, porcentajeDescuento, descripcionProducto);
        if (regProd.agregarProducto(prod)) {
            JOptionPane.showMessageDialog(null,"Producto ingresado","Ingresar Producto",1);
        }
        else{
            JOptionPane.showMessageDialog(null,"Producto No ingresado","Ingresar Producto",0);
        }
    }//GEN-LAST:event_jbtn_guardar1ActionPerformed

