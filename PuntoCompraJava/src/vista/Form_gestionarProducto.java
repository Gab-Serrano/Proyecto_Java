    private void jbtn_listar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_listar2ActionPerformed

        int precioUnitario,stock,codCategoria,numRazonSocialProv;
        double porcentajeDescuento;
        String descripcionProducto, codProducto;
        
        RegistroProducto rp = new RegistroProducto();
        DefaultTableModel modelo = (DefaultTableModel) this.jtbl_productos.getModel();
        modelo.setRowCount(0);
        List<Producto> lista = rp.buscarTodos();
        
        for (Producto producto : lista) {
            codProducto = producto.getCodProducto();
            descripcionProducto = producto.getDescripcionProducto();
            precioUnitario = producto.getPrecioUnitario();
            stock = producto.getStock();
            codCategoria  = producto.getCodigoCategoria();
            numRazonSocialProv = producto.getNumRazonSocialProv();
            porcentajeDescuento = producto.getPorcentajeDescuento();
            
            modelo.addRow(new Object[]{codProducto,descripcionProducto,precioUnitario,stock,codCategoria,numRazonSocialProv,porcentajeDescuento});
            
        }
    }//GEN-LAST:event_jbtn_listar2ActionPerformed

