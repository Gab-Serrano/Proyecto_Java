    private void jbtn_listarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_listarActionPerformed

        String dvRazonSocialProv,nombreProv,emailProv,direccionProv;
        int telefonoProv,numRazonSocialProv,codRegion,codProvincia,codComuna;
        RegistroProveedor rprov = new RegistroProveedor();
        DefaultTableModel modelo = (DefaultTableModel) this.jtbl_proveedor.getModel();
        modelo.setRowCount(0);
        List<Proveedor> listaProv = rprov.buscarTodosProveedores();
        
        for (Proveedor proveedor : listaProv) {
            numRazonSocialProv = proveedor.getNumRazonSocialProv();
            nombreProv = proveedor.getNombreProv();
            telefonoProv = proveedor.getTeleFonoProv();
            dvRazonSocialProv = proveedor.getDvRazonSocialprov();
            emailProv  = proveedor.getEmailProv();
            direccionProv = proveedor.getDireccionProv();  
            codRegion = proveedor.getCodRegion();
            codComuna = proveedor.getCodComuna();
            codProvincia = proveedor.getCodProvincia();
            modelo.addRow(new Object[]{numRazonSocialProv,dvRazonSocialProv,nombreProv,emailProv,direccionProv,codRegion,codComuna,codProvincia,telefonoProv});
            
        }
    }//GEN-LAST:event_jbtn_listarActionPerformed

